print("0.일  1.월  2.화  3.수  4.목  5.금  6.토")
print("0~6을 입력하여 요일을 출력하세요")
day = int(input())

if day == 0:
    print("일요일")

elif day == 1:
    print("월요일")

elif day == 2:
    print("화요일")

elif day == 3:
    print("수요일")

elif day == 4:
    print("목요일")

elif day == 5:
    print("금요일")

elif day == 6:
    print("토요일")
